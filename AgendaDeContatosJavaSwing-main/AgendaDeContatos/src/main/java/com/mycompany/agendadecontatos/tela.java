    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         if (!validarCampos()) return;

        Contato novoContato = new Contato(jTextField1.getText(), jTextField2.getText(), jTextField3.getText());
        contatos.add(novoContato);

        atualizarLista();
        JOptionPane.showMessageDialog(this, "Contato adicionado com sucesso!", "Confirmação", JOptionPane.INFORMATION_MESSAGE);

        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");// TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int selectedIndex = jList1.getSelectedIndex();
        if (selectedIndex >= 0) {
            Contato contato = contatos.get(selectedIndex);
            contato.setNome(jTextField1.getText());
            contato.setTelefone(jTextField2.getText());
            contato.setEmail(jTextField3.getText());

            atualizarLista();
            JOptionPane.showMessageDialog(this, "Contato atualizado com sucesso!", "Confirmação", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um contato para editar!", "Aviso", JOptionPane.WARNING_MESSAGE);
        } // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int selectedIndex = jList1.getSelectedIndex();
        if (selectedIndex >= 0) {
            contatos.remove(selectedIndex);
            atualizarLista();
            JOptionPane.showMessageDialog(this, "Contato removido com sucesso!", "Confirmação", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um contato para remover!", "Aviso", JOptionPane.WARNING_MESSAGE);
        }// TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed

