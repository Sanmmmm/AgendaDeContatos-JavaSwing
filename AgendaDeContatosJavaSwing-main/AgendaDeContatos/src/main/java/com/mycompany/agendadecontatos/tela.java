  public class Contato {
        private String nome;
        private String telefone;
        private String email;

        public Contato(String nome, String telefone, String email) {
            this.nome = nome;
            this.telefone = telefone;
            this.email = email;
        }

        public String getNome() {
            return nome;
        }

        public String getTelefone() {
            return telefone;
        }

        public String getEmail() {
            return email;
        }

        public void setNome(String nome) {
            this.nome = nome;
        }

        public void setTelefone(String telefone) {
            this.telefone = telefone;
        }

        public void setEmail(String email) {
            this.email = email;
        }

        @Override
        public String toString() {
            return "NOME:  " + nome + "     TEL: " + telefone+ "    E-MAIL:"+email;
        }
    }
   private boolean validarCampos() {
        if (jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nome e telefone são obrigatórios!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        if (!Pattern.matches("\\d+", jTextField2.getText())) {
            JOptionPane.showMessageDialog(this, "Telefone deve conter apenas números!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        if (!jTextField3.getText().isEmpty() && !Pattern.matches("\\S+@\\S+\\.\\S+", jTextField3.getText())) {
            JOptionPane.showMessageDialog(this, "Formato de e-mail inválido!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        return true;
    }

    private void atualizarLista() {
        DefaultListModel<String> model = new DefaultListModel<>();
        for (Contato contato : contatos) {
            model.addElement(contato.toString());
        }
        jList1.setModel(model);
    }
